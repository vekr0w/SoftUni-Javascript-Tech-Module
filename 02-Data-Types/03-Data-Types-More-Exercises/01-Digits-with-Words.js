function wordToNumConvert(input) {
  let printOutput = 0;
  switch (input) {
    case 'one':
      printOutput = 1;
      break;
    case 'two':
      printOutput = 2;
      break;
    case 'three':
      printOutput = 3;
      break;
    case 'four':
      printOutput = 4;
      break;
    case 'five':
      printOutput = 5;
      break;
    case 'six':
      printOutput = 6;
      break;
    case 'seven':
      printOutput = 7;
      break;
    case 'eight':
      printOutput = 8;
      break;
    case 'nine':
      printOutput = 9;
      break;
    case 'ten':
      printOutput = 10;
      break;

    default:
      break;
  }
  console.log(printOutput);

}